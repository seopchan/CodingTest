# 대각선에서 패턴 찾기
# 1. 대각선의 길이를 증가하면서 n이 해당 대각선에 있는지 확인
# 2. 대각선의 시작 지점에서 n번째 위치 계산
# 3. 대각선의 번호가 홀수인지 짝수인지에 따라 x, y 좌표 계산

n = int(input())

d = 1

# n번째 대각선에서 위치 찾기
while n > d:
    n -= d
    d += 1

# 대각선의 번호가 홀수일 때, 짝수일 때 위치 계산
if d % 2 == 0:
    x = n
    y = d - n + 1
else:
    x = d - n + 1
    y = n
    
print(f"{x}/{y}")